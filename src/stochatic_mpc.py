# ─────────────────────────────────────────────────────────────────────────────
# Scaled Frobenius norm / inner product — paper's convention (p. 5)
# ─────────────────────────────────────────────────────────────────────────────
def sfrob_inner(A: np.ndarray, B: np.ndarray) -> float:
    """⟨A, B⟩ = Tr(A Bᵀ) / p."""
    p = A.shape[0]
    return float(np.einsum("ij,ij->", A, B) / p)


def sfrob_norm_sq(A: np.ndarray) -> float:
    """||A||² = Tr(A Aᵀ) / p, so that ||I_p||² = 1."""
    return sfrob_inner(A, A)


# ─────────────────────────────────────────────────────────────────────────────
# Reshape a long hourly series into weekly columns
# ─────────────────────────────────────────────────────────────────────────────
def reshape_to_weekly(y: np.ndarray, hours_per_sample: int = 168) -> np.ndarray:
    """
    Return X of shape (p, n) where p = hours_per_sample and n = T // p.
    Each column is a consecutive, non-overlapping weekly profile.
    Trailing hours that don't fill a full week are discarded.
    """
    T = y.size
    n = T // hours_per_sample
    assert n >= 2, f"need >=2 full weeks, got T={T}"
    return y[: n * hours_per_sample].reshape(n, hours_per_sample).T


# ─────────────────────────────────────────────────────────────────────────────
# Ledoit-Wolf shrinkage estimator
# ─────────────────────────────────────────────────────────────────────────────
class LedoitWolfEstimator:
    """
    Fit a shrinkage covariance to a (p × n) data matrix whose columns are
    i.i.d. draws of the p-dim random vector.  The sample mean is removed
    (paper assumes mean-zero data) and stored as `mean_` so that samples
    can be returned in the original (non-centered) scale.

    Attributes after fit
    --------------------
    p, n                  : dimensions
    mean_                 : (p,)      sample mean profile
    S                     : (p,p)    sample covariance on centered data
    Sigma                 : (p,p)    shrinkage estimator Σ̃n
    m, d2, b2_bar, b2, a2 : the scalar estimators from the paper
    shrinkage_intensity   : b2/d2 ∈ [0,1]  (weight on identity target)
    """

    def __init__(self):
        self.p = None
        self.n = None

    def fit(self, X: np.ndarray) -> "LedoitWolfEstimator":
        assert X.ndim == 2, "X must be (p, n)"
        p, n = X.shape
        self.p, self.n = p, n

        # Center: paper's formulae assume E[x] = 0
        self.mean_ = X.mean(axis=1)
        Xc = X - self.mean_[:, None]

        # Sample covariance (paper uses n, not n-1)
        S = (Xc @ Xc.T) / n
        self.S = S

        # Scalar estimators — all in the scaled Frobenius metric.
        I_p = np.eye(p)
        m = sfrob_inner(S, I_p)                       # E[m] → μn
        d2 = sfrob_norm_sq(S - m * I_p)               # E[d2] → δ²n

        # b̄²n = (1/n²) Σ_k ||xk xkᵀ − S||²
        # Vectorise by noting each xk xkᵀ is a rank-1 pxp matrix.
        b2_bar = 0.0
        for k in range(n):
            xk = Xc[:, k]
            outer_minus_S = np.outer(xk, xk) - S
            b2_bar += sfrob_norm_sq(outer_minus_S)
        b2_bar /= (n * n)

        b2 = min(b2_bar, d2)                          # E[b2] → β²n
        a2 = d2 - b2                                  # E[a2] → α²n

        # Shrinkage weights
        w_id = b2 / d2 if d2 > 0 else 1.0             # towards m·I
        w_s  = a2 / d2 if d2 > 0 else 0.0             # towards S

        self.m, self.d2 = m, d2
        self.b2_bar, self.b2, self.a2 = b2_bar, b2, a2
        self.shrinkage_intensity = w_id
        self.Sigma = w_id * m * I_p + w_s * S

        # Ensure symmetry (numerical hygiene before Cholesky)
        self.Sigma = 0.5 * (self.Sigma + self.Sigma.T)
        return self

    # ───────── Sampling ─────────────────────────────────────────────────
    def _chol(self) -> np.ndarray:
        """Cholesky factor with adaptive jitter for numerical SPD robustness."""
        p = self.p
        jitter = 1e-10 * max(np.trace(self.Sigma) / p, 1.0)
        for _ in range(10):
            try:
                return np.linalg.cholesky(self.Sigma + jitter * np.eye(p))
            except np.linalg.LinAlgError:
                jitter *= 10
        raise np.linalg.LinAlgError("Sigma not SPD even with jitter")

    def sample(self, n_scenarios: int, seed: int | None = None) -> np.ndarray:
        """
        Draw n_scenarios profiles from N(mean_, Sigma).
        Returns array of shape (p, n_scenarios).
        """
        rng = np.random.default_rng(seed)
        L = self._chol()
        Z = rng.standard_normal(size=(self.p, n_scenarios))
        return self.mean_[:, None] + L @ Z

    def sample_horizon(
        self,
        hour_of_week_start: int,
        horizon: int,
        n_scenarios: int,
        seed: int | None = None,
    ) -> np.ndarray:
        """
        Convenience for stochastic MPC: draw n_scenarios weekly profiles,
        then slice each one starting at `hour_of_week_start` for `horizon`
        hours (wrapping around the week if needed).
        Returns (horizon, n_scenarios).
        """
        assert 0 <= hour_of_week_start < self.p
        full = self.sample(n_scenarios=n_scenarios, seed=seed)      # (p, S)
        idx = (hour_of_week_start + np.arange(horizon)) % self.p    # (horizon,)
        return full[idx, :]


# ─────────────────────────────────────────────────────────────────────────────
# Seasonally-adjusted Ledoit-Wolf  — fixes non-stationary annual drift
# ─────────────────────────────────────────────────────────────────────────────
#
# A flat LW fit on 52 weeks of a *non-stationary* series (campus load) fails
# because the 52 "samples" are not i.i.d.: they contain a strong seasonal
# drift (e.g. here the weekly mean moves from 10 MW in winter to 17 MW in
# summer, contributing ~48% of total variance).  The sample covariance then
# includes a giant "shift the whole week up or down" eigen-direction, which
#   (a) inflates scenario variance (each sample gets a random seasonal shift),
#   (b) dampens the diurnal amplitude of the mean profile (winter+summer
#       average flatter than either season alone).
#
# Fix: write  L(t) = S(t) + R(t)  where S is a smooth annual baseline
# (low-pass) and R is the week-to-week residual.  Fit LW to R only, sample
# R stochastically, and add back the deterministic S(t) at the target week.
# ─────────────────────────────────────────────────────────────────────────────
def smooth_seasonal_baseline(
    y: np.ndarray,
    window_days: int = 14,
    mode: str = "diurnal",
) -> np.ndarray:
    """
    Compute a slowly-varying baseline S(t) that removes annual drift.

    Two modes:

    * mode="daily" — take the daily mean, smooth with a centered moving
      average of `window_days`, broadcast back to hourly resolution.
      Removes seasonal level drift but assigns the *same* value to all
      hours of a day, so the residual R = L - S still carries the diurnal
      cycle.  This means the LW mean_ has to represent the diurnal cycle,
      which is dampened to the annual average and doesn't adapt to season.

    * mode="diurnal" (default) — smooth per hour-of-day. For each of the
      24 hours, a centered moving average across the `window_days`
      neighbouring days gives the seasonally-local mean for that hour.
      The baseline thus has *both* seasonal level AND season-appropriate
      diurnal amplitude.  The LW residual is then almost pure stochastic
      week-to-week noise, not a mixture of seasonal shape + noise.
    """
    T = y.size
    assert T % 24 == 0, f"expected multiple of 24 hours, got {T}"
    days = T // 24
    half = window_days // 2
    kernel = np.ones(2 * half + 1) / (2 * half + 1)

    if mode == "daily":
        daily_mean = y.reshape(days, 24).mean(axis=1)
        padded = np.pad(daily_mean, half, mode="edge")
        smooth_daily = np.convolve(padded, kernel, mode="valid")
        return np.repeat(smooth_daily, 24)

    elif mode == "diurnal":
        L2d = y.reshape(days, 24)                              # (days, 24)
        padded = np.pad(L2d, ((half, half), (0, 0)), mode="edge")
        S2d = np.empty_like(L2d)
        for h in range(24):
            S2d[:, h] = np.convolve(padded[:, h], kernel, mode="valid")
        return S2d.reshape(-1)

    else:
        raise ValueError(f"unknown mode {mode!r}")


class SeasonalLedoitWolfEstimator:
    """
    Ledoit-Wolf shrinkage fit on the seasonally-adjusted residual.

    Fit:    R(t) = L(t) - S(t);   reshape R -> (168, n_weeks);   LW on R.
    Sample: L̂(t) = S(t) + R̂(t)   with R̂ drawn from LW on the residuals.

    The baseline S is stored at hourly resolution over the full training
    series, plus a per-week mean vector `season_per_week` so scenarios can
    be conditioned on *which* week of the year they represent.
    """

    def __init__(self, hours_per_sample: int = 168, window_days: int = 14):
        self.p = hours_per_sample
        self.window_days = window_days
        self.lw = LedoitWolfEstimator()

    def fit(self, y: np.ndarray) -> "SeasonalLedoitWolfEstimator":
        self.S_hourly = smooth_seasonal_baseline(y, window_days=self.window_days)
        R = y - self.S_hourly                            # (T,)
        self.R_weeks = reshape_to_weekly(R, self.p)      # (p, n_weeks)

        # Per-week baseline values (season context for each training week).
        S_weeks = reshape_to_weekly(self.S_hourly, self.p)  # (p, n_weeks)
        self.season_per_week = S_weeks.mean(axis=0)         # (n_weeks,)

        self.lw.fit(self.R_weeks)
        return self

    # ─── Sampling ─────────────────────────────────────────────────────────
    def sample(
        self,
        n_scenarios: int,
        target_week: int | None = None,
        seed: int | None = None,
    ) -> np.ndarray:
        """
        Return (p, n_scenarios) weekly scenarios.

        `target_week` selects which week of the training year the deterministic
        baseline S comes from (0 = first week of data). If None, the annual
        mean baseline is used (approximately what the flat LW would produce).
        """
        R_samples = self.lw.sample(n_scenarios, seed=seed)     # (p, S)
        Sw = reshape_to_weekly(self.S_hourly, self.p)            # (p, n_weeks)
        if target_week is None:
            S = Sw.mean(axis=1)                                  # annual avg
        else:
            target_week = target_week % Sw.shape[1]
            S = Sw[:, target_week]
        return S[:, None] + R_samples

    def sample_horizon(
        self,
        t_start: int,
        horizon: int,
        n_scenarios: int,
        seed: int | None = None,
    ) -> np.ndarray:
        """
        Scenarios over `horizon` hours starting at absolute training-time
        index `t_start`.  The baseline is taken exactly from that offset of
        the fitted S(t), so the seasonal level is correct for the call site.

        Returns (horizon, n_scenarios).
        """
        hour_of_week = t_start % self.p
        R_scen = self.lw.sample_horizon(hour_of_week, horizon, n_scenarios, seed=seed)
        # Deterministic baseline over the horizon — wraps if horizon goes
        # past the end of the training series.
        T = self.S_hourly.size
        idx = (t_start + np.arange(horizon)) % T
        S = self.S_hourly[idx]                           # (horizon,)
        return S[:, None] + R_scen


# ─────────────────────────────────────────────────────────────────────────────
# Empirical log-normal  — for FR capacity price π_f (Sec. IV.C)
# ─────────────────────────────────────────────────────────────────────────────
class EmpiricalLogNormalSampler:
    """
    Fits μ_log, σ_log from the empirical distribution of log(π_f) and samples
    π_f ~ exp(N(μ_log, σ_log²)).  Strictly positive and heavy-tailed, matching
    the paper's observation that FR price "has high variance".
    """

    def __init__(self, clip_quantile: float = 0.999):
        # Cap very extreme tail samples to avoid absurd $/kW outliers that
        # would destabilise the LP.  Set to 1.0 to disable.
        self.clip_quantile = clip_quantile

    def fit(self, x: np.ndarray) -> "EmpiricalLogNormalSampler":
        x = np.asarray(x, dtype=float)
        x = x[x > 0]                                  # log-normal needs positive support
        assert x.size > 2, "need at least 3 positive observations"
        logx = np.log(x)
        self.mu_log = float(logx.mean())
        self.sigma_log = float(logx.std(ddof=1))
        self.data_ = x
        # Convenience moments in original scale
        self.mean_ = float(np.exp(self.mu_log + 0.5 * self.sigma_log ** 2))
        self.median_ = float(np.exp(self.mu_log))
        self.var_ = float((np.exp(self.sigma_log ** 2) - 1.0)
                          * np.exp(2 * self.mu_log + self.sigma_log ** 2))
        self._clip_hi = (float(np.quantile(x, self.clip_quantile))
                         if self.clip_quantile < 1.0 else np.inf)
        return self

    def sample(self, shape, seed: int | None = None) -> np.ndarray:
        """Sample from the fitted log-normal. `shape` can be int or tuple."""
        rng = np.random.default_rng(seed)
        z = rng.standard_normal(size=shape)
        x = np.exp(self.mu_log + self.sigma_log * z)
        return np.minimum(x, self._clip_hi)


# ─────────────────────────────────────────────────────────────────────────────
# Empirical Gaussian  — for hourly FR signal α (Sec. IV.C)
# ─────────────────────────────────────────────────────────────────────────────
class EmpiricalGaussianSampler:
    """
    Fits μ, σ from the empirical distribution of α and samples iid N(μ, σ²).
    The paper notes α "approximately follows a Gaussian distribution, which
    indicates that there is small temporal correlation", so iid sampling is
    consistent with that modelling assumption.  Samples are clipped to the
    physical bound [-1, 1].
    """

    def fit(self, x: np.ndarray) -> "EmpiricalGaussianSampler":
        x = np.asarray(x, dtype=float)
        self.mu = float(x.mean())
        self.sigma = float(x.std(ddof=1))
        self.data_ = x
        return self

    def sample(self, shape, seed: int | None = None) -> np.ndarray:
        rng = np.random.default_rng(seed)
        z = rng.standard_normal(size=shape)
        return np.clip(self.mu + self.sigma * z, -1.0, 1.0)


# ─────────────────────────────────────────────────────────────────────────────
# Goodness-of-fit validation
# ─────────────────────────────────────────────────────────────────────────────
def validate_lognormal(x: np.ndarray, fit: EmpiricalLogNormalSampler) -> dict:
    """
    Tests that x is log-normal with the parameters in `fit`.

    Strategy: a variable is log-normal iff log(x) is normal.  We therefore
    run normality tests on the log-transformed data plus a K-S test against
    the fitted lognormal CDF.
    """
    logx = np.log(x[x > 0])

    # Shapiro-Wilk can only handle n <= 5000, so subsample if needed.
    rng = np.random.default_rng(0)
    sw_sample = logx if logx.size <= 5000 else rng.choice(logx, 5000, replace=False)
    sw_stat, sw_p = stats.shapiro(sw_sample)

    # Anderson-Darling — no sample-size limit; crit. values at 15/10/5/2.5/1%
    ad = stats.anderson(logx, dist="norm")

    # K-S against fitted lognormal in original scale
    ks = stats.kstest(x, "lognorm",
                      args=(fit.sigma_log, 0.0, np.exp(fit.mu_log)))

    return dict(
        n=int(x.size),
        skew_log=float(stats.skew(logx)),
        excess_kurt_log=float(stats.kurtosis(logx)),  # 0 = Gaussian
        shapiro_stat=float(sw_stat),
        shapiro_p=float(sw_p),
        ad_stat=float(ad.statistic),
        ad_crit_5pct=float(ad.critical_values[2]),    # 5% significance level
        ks_stat=float(ks.statistic),
        ks_p=float(ks.pvalue),
        fitted_mu_log=fit.mu_log,
        fitted_sigma_log=fit.sigma_log,
    )


def validate_gaussian(x: np.ndarray, fit: EmpiricalGaussianSampler) -> dict:
    """Same structure as above but testing x directly for normality."""
    rng = np.random.default_rng(0)
    sw_sample = x if x.size <= 5000 else rng.choice(x, 5000, replace=False)
    sw_stat, sw_p = stats.shapiro(sw_sample)
    ad = stats.anderson(x, dist="norm")
    ks = stats.kstest(x, "norm", args=(fit.mu, fit.sigma))
    return dict(
        n=int(x.size),
        skew=float(stats.skew(x)),
        excess_kurt=float(stats.kurtosis(x)),
        shapiro_stat=float(sw_stat),
        shapiro_p=float(sw_p),
        ad_stat=float(ad.statistic),
        ad_crit_5pct=float(ad.critical_values[2]),
        ks_stat=float(ks.statistic),
        ks_p=float(ks.pvalue),
        fitted_mu=fit.mu,
        fitted_sigma=fit.sigma,
    )


def _verdict(diag: dict, kind: str) -> str:
    """Plain-English read of the test battery."""
    ad_ok = diag["ad_stat"] < diag["ad_crit_5pct"]
    # K-S p > 0.05 → can't reject null that x follows fitted distribution
    ks_ok = diag["ks_p"] > 0.05
    skew = diag.get("skew_log" if kind == "log-normal" else "skew", 0.0)
    kurt = diag.get("excess_kurt_log" if kind == "log-normal" else "excess_kurt", 0.0)

    bits = []
    bits.append(f"K-S  {'PASS' if ks_ok else 'REJECT'} (p={diag['ks_p']:.3g})")
    bits.append(f"AD   {'PASS' if ad_ok else 'REJECT'} "
                f"({diag['ad_stat']:.2f} vs crit@5%={diag['ad_crit_5pct']:.2f})")
    bits.append(f"skew={skew:+.2f}, excess_kurt={kurt:+.2f}")
    return "  |  ".join(bits)


# ─────────────────────────────────────────────────────────────────────────────
# Demo: fit to real campus load and visualise samples against paper Fig. 5
# ─────────────────────────────────────────────────────────────────────────────
def _plot_fit(X: np.ndarray, est: LedoitWolfEstimator, scenarios: np.ndarray,
              out_path: str):
    p, n = X.shape
    hours = np.arange(p)
    fig, axs = plt.subplots(2, 1, figsize=(12, 8), sharex=True)

    # Top — observed weekly profiles overlaid, with mean (Fig. 5 middle)
    for k in range(n):
        axs[0].plot(hours, X[:, k], color="steelblue", lw=0.6, alpha=0.35)
    axs[0].plot(hours, est.mean_, color="black", lw=2.0, label="Weekly mean profile")
    axs[0].set_ylabel("Load (kW)")
    axs[0].set_title(f"52 weekly load profiles + mean  (p={p}, n={n})")
    axs[0].legend(loc="upper right")
    axs[0].grid(alpha=0.3)

    # Bottom — sampled scenarios vs mean vs one held-out true week (Fig. 5 bottom)
    true_idx = 10
    for s in range(scenarios.shape[1]):
        axs[1].plot(hours, scenarios[:, s], color="tomato", lw=0.6, alpha=0.30)
    axs[1].plot(hours, est.mean_,   color="black", lw=2.0, label="Mean scenario")
    axs[1].plot(hours, X[:, true_idx], color="royalblue", lw=1.8, ls="--",
                label=f"True profile (week {true_idx})")
    axs[1].set_xlabel("Hour of week")
    axs[1].set_ylabel("Load (kW)")
    axs[1].set_title(f"{scenarios.shape[1]} Ledoit-Wolf scenarios  "
                     f"(shrinkage intensity = {est.shrinkage_intensity:.3f})")
    axs[1].legend(loc="upper right")
    axs[1].grid(alpha=0.3)

    plt.tight_layout()
    plt.savefig(out_path, dpi=130)
    plt.close()


def _plot_scalar_fit(
    x: np.ndarray,
    samples: np.ndarray,
    pdf_fn,
    title: str,
    xlabel: str,
    out_path: str,
    logx: bool = False,
):
    """Histogram + fitted PDF + Q-Q plot, side-by-side."""
    fig, axs = plt.subplots(1, 2, figsize=(13, 4.5))

    # Left — histogram of data + fitted PDF + histogram of samples (dashed)
    bins = 60
    axs[0].hist(x, bins=bins, density=True, alpha=0.45, color="steelblue",
                label=f"data (n={x.size})")
    axs[0].hist(samples, bins=bins, density=True, histtype="step",
                color="tomato", lw=1.4, label=f"samples (n={samples.size})")
    xs = np.linspace(x.min(), x.max(), 400)
    axs[0].plot(xs, pdf_fn(xs), color="black", lw=2.0, label="fitted PDF")
    if logx:
        axs[0].set_xscale("log")
    axs[0].set_xlabel(xlabel); axs[0].set_ylabel("density")
    axs[0].set_title(title)
    axs[0].legend(loc="upper right", fontsize=9)
    axs[0].grid(alpha=0.3)

    # Right — Q-Q vs theoretical quantiles
    if logx:
        stats.probplot(np.log(x[x > 0]), dist="norm", plot=axs[1])
        axs[1].set_title("Q-Q plot of log(x) vs Normal")
    else:
        stats.probplot(x, dist="norm", plot=axs[1])
        axs[1].set_title("Q-Q plot of x vs Normal")
    axs[1].grid(alpha=0.3)

    plt.tight_layout()
    plt.savefig(out_path, dpi=130)
    plt.close()


