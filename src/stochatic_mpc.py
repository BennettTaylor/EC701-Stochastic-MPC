"""
Note that Claude was used to assist with writing this code.

Stochastic MPC scenario generators.

Three distributions — one per uncertain quantity — following Kumar et al.:

  1. LedoitWolfEstimator        (load)       — Sec. IV.B, p. 5
     Multivariate Gaussian over the 168-h week profile with shrinkage
     covariance Σ̃n = (b²/d²)·m·I + (a²/d²)·S so it stays full-rank
     when p > n.

  2. EmpiricalLogNormalSampler  (FR price)   — Sec. IV.C, p. 6
     "We have found that the FR price follows a log-normal distribution
      and has high variance ... we use the empirical log-normal
      distribution to generate FR price realizations."
     Fit μ_log = mean(log π_f), σ_log = std(log π_f); sample exp(N(μ, σ²)).

  3. EmpiricalGaussianSampler   (FR signal)  — Sec. IV.C, p. 6
     "The signal approximately follows a Gaussian distribution, which
      indicates that there is small temporal correlation ... realizations
      generated by sampling from the empirical Gaussian."
     Fit μ, σ from data; sample iid N(μ, σ²).

The bottom of the file validates each fit against the real data served
by data.py (Shapiro-Wilk / Anderson-Darling / K-S, moments, Q-Q plot).

See also:


Ledoit-Wolf details:

  Observations xk ∈ R^p (k=1..n) assumed drawn from a zero-mean
  distribution with unknown covariance Σn.  The sample covariance
  Sn = Xn Xn^T / n is rank-deficient here because p=168 (hours in a
  week) >> n=52 (weeks in one year of data).

  The paper shrinks Sn towards a scaled identity target:

      Σ*_n = ρ1 · I + ρ2 · Sn

  minimising E[||Σ*_n - Σn||²] under the scaled Frobenius norm
  ||A||² = Tr(A Aᵀ) / p (so that ||I_p||² = 1).  The optimal
  coefficients depend on unknown quantities of Σn and are replaced
  by bona-fide estimators:

      mn   = ⟨Sn, I⟩                              (sample diag-mean)
      d²n  = ||Sn - mn·I||²
      b̄²n  = (1/n²) Σ_k ||xk xkᵀ - Sn||²
      b²n  = min(b̄²n, d²n)
      a²n  = d²n - b²n

  Shrinkage estimator:

      Σ̃n = (b²n / d²n) · mn · I + (a²n / d²n) · Sn

  The weight b²n/d²n ∈ [0,1] is the shrinkage intensity (how much we
  pull towards the identity target).

We use this to model the *centered* week profile (raw load minus the
per-hour-of-week mean).  A scenario is drawn by sampling a p-dim
Gaussian with mean = weekly-mean-profile and covariance = Σ̃n; a slice
of that weekly scenario can then be served to the stochastic MPC
over its N-hour prediction horizon.
"""

