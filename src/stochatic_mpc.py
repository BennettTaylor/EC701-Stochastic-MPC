    print("\nLedoit-Wolf scalar estimators:")
    print(f"  m   (⟨S,I⟩)          = {est.m:.4e}  kW²")
    print(f"  d²  (||S - mI||²)    = {est.d2:.4e}")
    print(f"  b̄²  (raw)            = {est.b2_bar:.4e}")
    print(f"  b²  = min(b̄², d²)   = {est.b2:.4e}   (→ β²n)")
    print(f"  a²  = d² - b²        = {est.a2:.4e}   (→ α²n)")
    print(f"  shrinkage b²/d²      = {est.shrinkage_intensity:.4f}  "
          f"(0 = pure sample cov, 1 = identity target)")

    eigs = np.linalg.eigvalsh(est.Sigma)
    print(f"\nΣ̃n eigenvalues: min={eigs.min():.3e}, max={eigs.max():.3e}, "
          f"cond={eigs.max()/max(eigs.min(),1e-30):.2e}")

    # Draw scenarios
    scenarios = est.sample(n_scenarios=50, seed=0)
    print(f"\nSampled {scenarios.shape[1]} full-week scenarios, shape={scenarios.shape}")

    # Slice example for a stochastic-MPC horizon
    hour_of_week = 36          # e.g. Monday 12:00
    N = 24
    horizon_scenarios = est.sample_horizon(hour_of_week, N, n_scenarios=50, seed=1)
    print(f"Horizon-slice scenarios at hour_of_week={hour_of_week}, N={N}: "
          f"shape={horizon_scenarios.shape}")

    # Plot fit + samples (flat LW)
    root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    out = os.path.join(root, "ledoit_wolf_load.png")
    _plot_fit(X, est, scenarios, out)
    print(f"\nPlot saved -> {out}")

    # ─────────────────────────────────────────────────────────────────────
    # Seasonally-adjusted LW — compare against flat LW
    # ─────────────────────────────────────────────────────────────────────
    print("\n" + "=" * 72)
    print("LOAD — Seasonally-adjusted Ledoit-Wolf")
    print("=" * 72)
    sest = SeasonalLedoitWolfEstimator(hours_per_sample=168, window_days=14).fit(L)
    print(f"  baseline window      : {sest.window_days} days")
    print(f"  season range         : {sest.season_per_week.min():.0f} → "
          f"{sest.season_per_week.max():.0f} kW  "
          f"(flat LW swallowed this as noise)")
    print(f"  residual mean        : {sest.R_weeks.mean():+.0f} kW   "
          f"(≈ 0 by construction)")
    print(f"  residual std         : {sest.R_weeks.std():.0f} kW   "
          f"(vs raw weekly std {X.std():.0f})")

    eigs_s = np.linalg.eigvalsh(sest.lw.Sigma)
    flat_per_hr = np.sqrt(np.trace(est.Sigma) / 168)
    seas_per_hr = np.sqrt(np.trace(sest.lw.Sigma) / 168)
    print(f"  LW on residual:")
    print(f"    shrinkage intensity = {sest.lw.shrinkage_intensity:.4f}   "
          f"(flat LW = {est.shrinkage_intensity:.4f})")
    print(f"    top eigenvalue      = {eigs_s[-1]:.2e}   "
          f"(flat LW top = {eigs[-1]:.2e})")
    print(f"    per-hr implied std  = {seas_per_hr:.0f} kW   "
          f"(flat LW = {flat_per_hr:.0f} kW)")

    # Helper: lag-1 autocorrelation of a 1-D sequence
    def _lag1_ac(y):
        y = np.asarray(y, dtype=float)
        y = y - y.mean()
        denom = (y * y).sum()
        if denom <= 0:
            return 0.0
        return float((y[:-1] * y[1:]).sum() / denom)

    # Helper: diurnal amplitude = mean(peak-of-day) - mean(trough-of-day)
    def _diurnal_amp(y, p=168):
        d = y.reshape(-1, 24)                   # (7 days, 24 hrs) within a week
        return float(d.max(axis=1).mean() - d.min(axis=1).mean())

    # Compare sampled variability, periodicity (lag-1 AC), and diurnal amplitude
    # to real weeks. These quantify the three complaints: (1) variance too high,
    # (2) no periodicity, (3) no seasonal adaptation.
    print(f"\n  {'week':>5s}  {'real_std':>9s}  {'samp_std':>9s}   "
          f"{'real_AC1':>9s}  {'samp_AC1':>9s}   "
          f"{'real_amp':>9s}  {'samp_amp':>9s}")
    for wk in [10, 30]:
        real_week = X[:, wk]
        scen_wk = sest.sample(n_scenarios=200, target_week=wk, seed=42)   # (168, 200)
        samp_std = scen_wk.std(axis=0).mean()
        samp_ac  = np.mean([_lag1_ac(scen_wk[:, s]) for s in range(scen_wk.shape[1])])
        samp_amp = np.mean([_diurnal_amp(scen_wk[:, s]) for s in range(scen_wk.shape[1])])
        print(f"  {wk:5d}  {real_week.std():>9.0f}  {samp_std:>9.0f}   "
              f"{_lag1_ac(real_week):>9.3f}  {samp_ac:>9.3f}   "
              f"{_diurnal_amp(real_week):>9.0f}  {samp_amp:>9.0f}")

